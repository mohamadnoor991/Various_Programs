package com.semweb.semWebArt.model;

public class Museums {

    String museumLabel;
    String museumDescription ;
    String villeIdLabel;
    String ville;
    double lat;
    String latN;
    double lon;
    String lonN;
    public String getMuseumLabel() {
        return museumLabel;
    }

    public void setMuseumLabel(String museumLabel) {
        this.museumLabel = museumLabel;
    }

    public String getMuseumDescription() {
        return museumDescription;
    }

    public void setMuseumDescription(String museumDescription) {
        this.museumDescription = museumDescription;
    }

    public String getVilleIdLabel() {
        return villeIdLabel;
    }

    public void setVilleIdLabel(String villeIdLabel) {
        this.villeIdLabel = villeIdLabel;
    }

    public String getVille() {
        return ville;
    }

    public void setVille(String ville) {
        this.ville = ville;
    }

    public double getLat() {
        return lat;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }

    public double getLon() {
        return lon;
    }

    public void setLon(double lon) {
        this.lon = lon;
    }

    public String getLatN() {
        return latN;
    }

    public void setLatN(String latN) {
        this.latN = latN;
    }

    public String getLonN() {
        return lonN;
    }

    public void setLonN(String lonN) {
        this.lonN = lonN;
    }
}
